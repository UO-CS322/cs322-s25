numbers = [1, 2, 3, 4, 5]

# Loop 1: Squaring the numbers
squared_numbers = []
for num in numbers:
    squared_numbers.append(num**2)

# Loop 2: Doubling the numbers
doubled_numbers = []
for num in numbers:
    doubled_numbers.append(num * 2)

print("Squared numbers:", squared_numbers)
print("Doubled numbers:", doubled_numbers)
